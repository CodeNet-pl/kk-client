<?php
namespace CodeNet\KKClient\Resource;

use CodeNet\KKClient\Resource;

class Service extends Resource
{
    /**
     * @var string
     */
    protected $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}